var Web3 = require('web3');

const ArtDeFiPool = artifacts.require("ArtDeFiPool");
const ArtDeFiEvaluator = artifacts.require("ArtDeFiEvaluator");

// pools
const PROVIDER_KOVAN = '0x506B0B2CF20FAA8f38a4E2B524EE43e1f4458Cc5';
const LIQ_RESERVE_KOVAN = '0xFf795577d9AC8bD7D90Ee22b6C1703490b6512FD';
const LIQ_ATOKEN_KOVAN = '0x58AD4cB396411B691A9AAb6F74545b2C5217FE6a';
const LOAN_RESERVE_KOVAN = '0xFf795577d9AC8bD7D90Ee22b6C1703490b6512FD';

const PROVIDER_MAINNET = '0x24a42fD28C976A61Df5D00D0599C34c4f90748c8';
const LIQ_RESERVE_MAINNET = '0x6B175474E89094C44Da98b954EedeAC495271d0F';
const LIQ_ATOKEN_MAINNET = '0xfC1E690f61EFd961294b3e1Ce3313fBD8aa4f85d';
const LOAN_RESERVE_MAINNET = '0xFf795577d9AC8bD7D90Ee22b6C1703490b6512FD';

// chainlink oracles
const CHAINLINK_ORACLE_KOVAN = '0x2f90A6D021db21e1B2A077c5a37B3C7E75D15b7e';
const CHAINLINK_JOB_ID_KOVAN = Web3.utils.asciiToHex('b9fd06bb42dd444db1b944849cbffb11');

const CHAINLINK_ORACLE_MAINNET = '0x89f70fA9F439dbd0A1BC22a09BEFc56adA04d9b4';
const CHAINLINK_JOB_ID_MAINNET = Web3.utils.asciiToHex('aca45ec53db44192bd874c5a0436598a');


module.exports = function(deployer, network) {
  if (network == 'kovan') {
    deployer.deploy(ArtDeFiPool, PROVIDER_KOVAN, LIQ_RESERVE_KOVAN, LIQ_ATOKEN_KOVAN, LOAN_RESERVE_KOVAN, {overwrite: false});
    deployer.deploy(ArtDeFiEvaluator, CHAINLINK_ORACLE_KOVAN, CHAINLINK_JOB_ID_KOVAN);
  } else if (network == 'mainnet') {
    deployer.deploy(ArtDeFiPool, PROVIDER_MAINNET, LIQ_RESERVE_MAINNET, LIQ_ATOKEN_MAINNET, LOAN_RESERVE_MAINNET);
    deployer.deploy(ArtDeFiEvaluator, CHAINLINK_ORACLE_MAINNET, CHAINLINK_JOB_ID_MAINNET);
  } else {
    deployer.deploy(ArtDeFiPool, PROVIDER_MAINNET, LIQ_RESERVE_MAINNET, LIQ_ATOKEN_MAINNET, LOAN_RESERVE_MAINNET);
    deployer.deploy(ArtDeFiEvaluator, CHAINLINK_ORACLE_MAINNET, CHAINLINK_JOB_ID_MAINNET);
  }
};
